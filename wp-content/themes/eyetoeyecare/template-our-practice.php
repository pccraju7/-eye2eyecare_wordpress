With deep roots in Houston, Dr. Momin was raised with humble beginnings and developed a passion for healthcare while attending the High School for Health Professions in Houston ISD. It was there that she became fascinated with the visual sciences and aspired to become an optometrist.

Dr. Momin attended the University of Houston where she first earned her Bachelor of Science in Biology in 1994 and went on to earn her Doctorate in Optometry from the University Of Houston College Of Optometry in 1998.

Dr. Momin completed an externship at the Houston Eye Associates where she assisted in peri-operative care of surgeries of the eyelids and tear ducts. She also spent time at Fort Benning U.S. Army Base in Georgia where she focused on primary eye care for American soldiers and their families.

In 2001, Dr. Momin established her private practice in Sugar Land, TX and has since been servicing the needs of the local community in regards to their visual health.

She is dedicated to her patients' well-being and values compassion, integrity, and goes the extra mile to provide quality care to her patients. Throughout her professional career, she has practiced a full scope of optometry which includes routine eye exams, fitting the latest contact lenses for children and adults, diagnosing and treating various eye conditions and diseases, as well as co-managing LASIK and cataract patients.

In her spare time, Dr. Momin enjoys traveling, photography, reading, watching sports, and spending time with her family and friends.

